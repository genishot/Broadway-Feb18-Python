
# Slicing = :
a = [2, 4, 6, 8, 10]
print(a[2:])

# clear = this function clears all the list values
x = [1, 2, 3, 4, 5, 6, 7]
x.clear()
print(x)

#copy (Very important)
y = [1,2,3,4,5,6]
z = y.copy()
print(z)

#Count  
b = [1,2,2,3,4,5,6]
b.count(2)
print(b)

#Reverse
k = [1,2,3,4,5,6]
k.reverse()
print(k)

#Sort
a = [100,25,34,10,5,500]
a.sort()
print(a)

#Inorder to put in descending order
a = [50,250,13,15,20,400,600,250]
a.sort(reverse=True)
print(a)

